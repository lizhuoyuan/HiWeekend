package com.geminno.JuHe;

public class MovieInfo {
	
	private String movieid;// ID
	private String rating;// 评分
	private String genres;// 影片分类
	private String runtime;// 影片持续时间
	private String language;// 影片语言
	private String title;// 影片名称
	private String poster;// 海报
	private String country;// 拍摄国家
	private String directors;// 导演
	private String plot_simple;// 剧情概要
	private String year;// 拍摄年代
	private String release_date;// 影片上映时间

	public void setMovieid(String movieid) {
		this.movieid = movieid;
	}

	public String getMovieid() {
		return this.movieid;
	}

	public void setRating(String rating) {
		this.rating = rating;
	}

	public String getRating() {
		return this.rating;
	}

	public void setGenres(String genres) {
		this.genres = genres;
	}

	public String getGenres() {
		return this.genres;
	}

	public void setRuntime(String runtime) {
		this.runtime = runtime;
	}

	public String getRuntime() {
		return this.runtime;
	}

	public void setLanguage(String language) {
		this.language = language;
	}

	public String getLanguage() {
		return this.language;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getTitle() {
		return this.title;
	}

	public void setPoster(String poster) {
		this.poster = poster;
	}

	public String getPoster() {
		return this.poster;
	}

	public void setDirectors(String directors) {
		this.directors = directors;
	}

	public String getDirectors() {
		return this.directors;
	}

	public void setPlot_simple(String plot_simple) {
		this.plot_simple = plot_simple;
	}

	public String getPlot_simple() {
		return this.plot_simple;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getYear() {
		return this.year;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getCountry() {
		return this.country;
	}

	public void setRelease_date(String release_date) {
		this.release_date = release_date;
	}

	public String getRelease_date() {
		return this.release_date;
	}

}
